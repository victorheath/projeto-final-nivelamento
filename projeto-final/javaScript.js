/* O que são Variáveis e Constantes?
Variáveis (var) é um espaço de memória onde podemos armazenar dados que podem ser alterados.
    var pessoa = "nome" --> posso mudar o nome, pois está dentro dessa variável
Constantes (const) também é um espaço de memória onde podemos armazenar dados, porém, não se pode alterar.
    const pessoa = "nome" --> não posso mudar o nome que está dentro dessa constante.
    */




